#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	size_t	count;

	count = ft_strlen(s1);
	i = 0;
	while (s2[i] != '\0')
	{
		s1[i + count] = s2[i];
		i++;
	}
	s1[i + count] = '\0';
	return (s1);
}
